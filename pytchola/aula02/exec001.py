'''
 Faça um programa que leia dois números e mostre a soma, a subtração, a multiplicação e a divisão entre eles. 
'''
num1 = int(input('Digite o primeiro número a ser calculado:'))
num2 = int(input('Digite o segundo número a ser calculado:'))
soma = num1 + num2
subt = num1 - num2
mult = num1 * num2
divi = num1 / num2
print('A soma dos dois valores é:', soma)
print('A subtração dos dois valores é:', subt)
print('A multiplicação de um valor pelo outro é:',mult)
print('Por fim, a divisão dos dois valores é:',divi)