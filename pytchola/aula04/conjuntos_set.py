# listas sao [] e dicionarios sao {} set sao {} tambem
# resumo: so da pra adicionar ou remover. sets sao imutaveis, e nao permitem itens duplicados. tambem nao tem indice, cada item é independente
thisset = {"apple", "banana", "cherry"}
print(thisset) 

thisset = {"apple", "banana", "cherry"}

thisset.add("orange") # como é sem ordem, ele entra no inicio do set

print(thisset) 